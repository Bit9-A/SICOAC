async function seed() {
  console.log('=== Carga de geografía de Venezuela ===')
  console.log('Parseando datos...')

  const data = parseData(RAW)
  let totalEstados = 0
  let totalMuns = 0
  let totalPars = 0

  console.log(`Encontrados ${data.length} estados`)

  // 1. Estados
  console.log('\n--- ESTADOS ---')
  const estadosToInsert = data.map(e => ({ nombre: e.name }))
  const { data: dbEstados, error: estErr } = await supabase
    .from('estado')
    .upsert(estadosToInsert, { onConflict: 'nombre' })
    .select('id, nombre')
  if (estErr) throw estErr
  totalEstados = dbEstados.length
  console.log(`✅ ${totalEstados} estados`)

  const estadoMap = {}
  dbEstados.forEach(e => { estadoMap[e.nombre] = e.id })

  // 2. Municipios
  console.log('\n--- MUNICIPIOS ---')
  const munsToInsert = []
  data.forEach(e => {
    const eId = estadoMap[e.name]
    if (eId) {
      e.municipios.forEach(m => {
        munsToInsert.push({ nombre: m.name, estado_id: eId })
      })
    }
  })

  const dbMuns = []
  for (let i = 0; i < munsToInsert.length; i += 100) {
    const batch = munsToInsert.slice(i, i + 100)
    const { data: inserted, error: munErr } = await supabase
      .from('municipio')
      .upsert(batch, { onConflict: 'nombre,estado_id' })
      .select('id, nombre, estado_id')
    if (munErr) throw munErr
    dbMuns.push(...inserted)
  }
  totalMuns = dbMuns.length
  console.log(`✅ ${totalMuns} municipios`)

  const munMap = {}
  dbMuns.forEach(m => { munMap[`${m.nombre}:${m.estado_id}`] = m.id })

  // 3. Parroquias
  console.log('\n--- PARROQUIAS ---')
  const parsToInsert = []
  data.forEach(e => {
    const eId = estadoMap[e.name]
    if (eId) {
      e.municipios.forEach(m => {
        const mId = munMap[`${m.name}:${eId}`]
        if (mId) {
          m.parroquias.forEach(p => {
            parsToInsert.push({ nombre: p, municipio_id: mId })
          })
        }
      })
    }
  })

  for (let i = 0; i < parsToInsert.length; i += 200) {
    const batch = parsToInsert.slice(i, i + 200)
    const { error: parErr } = await supabase
      .from('parroquia')
      .upsert(batch, { onConflict: 'nombre,municipio_id' })
    if (parErr) throw parErr
  }
  totalPars = parsToInsert.length
  console.log(`✅ ${totalPars} parroquias`)

  console.log(`\n🎉 CARGA COMPLETA:`)
  console.log(`   ${totalEstados} estados`)
  console.log(`   ${totalMuns} municipios`)
  console.log(`   ${totalPars} parroquias`)
}

seed().catch(err => {
  console.error('❌ Error:', err.message || err)
  process.exit(1)
})
