function decodeEntity(text) {
  return text
    .replace(/&NTILDE;/g, 'Ñ')
    .replace(/&ntilde;/g, 'ñ')
    .replace(/&#209;/g, 'Ñ')
    .replace(/&#241;/g, 'ñ')
    .replace(/PE#ALVER/g, 'PEÑALVER')
    .replace(/\?/g, 'Ó')
}

function parseData(raw) {
  const lines = raw.split('\n').map(l => l.trim()).filter(l => l)
  const states = []
  let currentState = null
  let currentMun = null

  for (const line of lines) {
    if (line.startsWith('## ')) {
      currentState = { name: decodeEntity(line.slice(3).trim()), municipios: [] }
      states.push(currentState)
      currentMun = null
    } else if (line.startsWith('### ')) {
      currentMun = { name: decodeEntity(line.slice(4).trim()), parroquias: [] }
      currentState.municipios.push(currentMun)
    } else if (line.startsWith('- ')) {
      let pName = line.slice(2).trim()
      // Remove prefix like "PQ. " or "CM. "
      pName = pName.replace(/^(PQ|CM)\.?\s*/i, '').trim()
      // Fix encoding artifacts
      pName = decodeEntity(pName)
        .replace(/PE#ALVER/g, 'PEÑALVER')
        .replace(/CA#A/g, 'CAÑA')
        .replace(/CA#O/g, 'CAÑO')
        .replace(/PE#A/g, 'PEÑA')
        .replace(/PE#ITAS/g, 'PEÑITAS')
        .replace(/BRICE#O/g, 'BRICEÑO')
        .replace(/MONTA#A/g, 'MONTAÑA')
        .replace(/MARI#O/g, 'MARIÑO')
        .replace(/PI#ANGO/g, 'PIÑANGO')
        .replace(/TU#AME/g, 'TUÑAME')
        .replace(/PE#ALOZA/g, 'PEÑALOZA')
        .replace(/URE#A/g, 'UREÑA')
      if (currentMun) {
        currentMun.parroquias.push(pName)
      }
    }
  }
  return states
}

