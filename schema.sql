-- 8. Producto Table
create table public.producto (
  id bigint generated by default as identity primary key,
  nombre text not null,
  descripcion text,
  presentacion text, -- E.g., 'Bolsa de 1kg', 'Envase de 1L'
  institucion_id bigint references public.institucion(id) on delete set null, -- Optional: owner/origin institution
  categoria_id bigint references public.categoria(id) on delete restrict not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create trigger update_producto_updated_at
  before update on public.producto
  for each row execute function public.update_updated_at_column();

-- 9. Producto Codigo Table (A product can have multiple barcodes)
create table public.producto_codigo (
  id bigint generated by default as identity primary key,
  producto_id bigint references public.producto(id) on delete cascade not null,
  codigo text not null unique,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create trigger update_producto_codigo_updated_at
  before update on public.producto_codigo
  for each row execute function public.update_updated_at_column();

-- 10. Movimiento Table (Tracks transfers between institutions, quantities and units)
create table public.movimiento (
  id bigint generated by default as identity primary key,
  producto_id bigint references public.producto(id) on delete restrict not null,
  cantidad numeric not null check (cantidad > 0),
  unidad text not null, -- E.g., 'kg', 'unidades', 'paquetes'
  institucion_origen_id bigint references public.institucion(id) on delete restrict, -- Null if external entry
  institucion_destino_id bigint references public.institucion(id) on delete restrict, -- Null if external exit
  tipo_movimiento_id bigint references public.tipo_movimiento(id) on delete restrict not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null,
  constraint check_different_institutions check (
    institucion_origen_id is null or 
    institucion_destino_id is null or 
    institucion_origen_id <> institucion_destino_id
  )
);

create trigger update_movimiento_updated_at
  before update on public.movimiento
  for each row execute function public.update_updated_at_column();

