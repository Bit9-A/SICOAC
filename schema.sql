-- 1. Automatic Timestamp Trigger
create or replace function public.update_updated_at_column()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

-- 2. Estado Table
create table public.estado (
  id bigint generated by default as identity primary key,
  nombre text not null unique,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create trigger update_estado_updated_at
  before update on public.estado
  for each row execute function public.update_updated_at_column();

-- 3. Municipio Table
create table public.municipio (
  id bigint generated by default as identity primary key,
  nombre text not null,
  estado_id bigint references public.estado(id) on delete cascade not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique (nombre, estado_id)
);

create trigger update_municipio_updated_at
  before update on public.municipio
  for each row execute function public.update_updated_at_column();

